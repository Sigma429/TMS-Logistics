<script>
import $ from 'jquery'

/* eslint-disable */
$(function () {
  clearTimeout(timer)
  function rgbToHex(r, g, b) {
    var hex = ((r << 16) | (g << 8) | b).toString(16)
    return '#' + new Array(Math.abs(hex.length - 7)).join('0') + hex
  }

  // hex to rgb
  function hexToRgb(hex) {
    var rgb = []
    for (var i = 1; i < 7; i += 2) {
      rgb.push(parseInt('0x' + hex.slice(i, i + 2)))
    }
    return rgb
  }

  // 计算渐变过渡色
  function gradient(startColor, endColor, step) {
    // 将 hex 转换为rgb
    var sColor = hexToRgb(startColor),
      eColor = hexToRgb(endColor)

    // 计算R\G\B每一步的差值
    var rStep = (eColor[0] - sColor[0]) / step
    var gStep = (eColor[1] - sColor[1]) / step
    var bStep = (eColor[2] - sColor[2]) / step

    var gradientColorArr = []
    for (var i = 0; i < step; i++) {
      // 计算每一步的hex值
      gradientColorArr.push(
        rgbToHex(
          parseInt(rStep * i + sColor[0]),
          parseInt(gStep * i + sColor[1]),
          parseInt(bStep * i + sColor[2])
        )
      )
    }
    return gradientColorArr
  }

  var startColor = '#FD827C'
  var endColor = '#FD827C'
  var colorArr = gradient(startColor, endColor, 32)

  // 内圆圈动画
  function percent(percent) {
    if (percent <= 50) {
      var xz = 45 + 3.6 * percent
      $('.rightcircle').css('transform', 'rotate(' + xz + 'deg)')
    } else if (percent <= 100) {
      var xz = 45 + 3.6 * (percent - 50)
      $('.rightcircle').css('transform', 'rotate(225deg)')
      $('.leftcircle').css('transform', 'rotate(' + xz + 'deg)')
    }
  }

  // 添加定时器
  var timer = setTimeout(yundong, 500)
  //对2秒计数
  var js = 1
  //当前是百分之几
  var baifenbi = 0
  var baifenbij = 0
  function yundong() {
    baifenbij = baifenbi
    if (js <= 2) {
      baifenbi = baifenbi + 20
      percent(baifenbi)
      shuzi()
    } else if (js <= 3) {
      baifenbi = baifenbi + 10
      percent(baifenbi)
      shuzi()
    } else if (js <= 13) {
      baifenbi = baifenbi + 2
      percent(baifenbi)
      shuzi()
    } else if (js <= 33) {
      baifenbi = baifenbi + 1
      percent(baifenbi)
      shuzi()
    }
    js++
  }

  function shuzi() {
    $({
      // 起始值
      countNum: baifenbij
    }).animate(
      {
        // 最终值
        countNum: baifenbi
      },
      {
        // 动画持续时间
        duration: 2000,
        easing: 'linear',
        step: function () {
          // 设置每步动画计算的数值
          $('.barPie__value').text(Math.round((this.countNum * 100) / 100))
          var djge = Math.floor(this.countNum / 3.125)
          $("[for='p1_barPieItem" + djge + "']")
            .find('div')
            .addClass('gaoliang')
          $("[for='p1_barPieItem" + djge + "']")
            .find('div')
            .css('background', colorArr[djge])
        },
        complete: function () {
          // 设置动画结束的数值
          $('.barPie__value').text(this.countNum)
        }
      }
    )
  }
})
export default {}
</script>

